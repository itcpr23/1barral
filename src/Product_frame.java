    public void refresh_data(){
        DefaultTableModel md = (DefaultTableModel) jTable1.getModel();
        md.setRowCount(0);
     try{
         Class.forName(con.forname);
         Connection cnn = (Connection) DriverManager.getConnection(con.driver,con.us, con.ps1);
         Statement st = (Statement) cnn.createStatement();
         ResultSet res = st.executeQuery("select * from products");
         while(res.next()){
             md.addRow(new Object[]{res.getString("id"),res.getString("product"),res.getString("quantity"),res.getString("price")});
         }
     } catch (ClassNotFoundException ex) {   
        Logger.getLogger(Product_frame.class.getName()).log(Level.SEVERE, null, ex);
    } catch (SQLException ex) {
        Logger.getLogger(Product_frame.class.getName()).log(Level.SEVERE, null, ex);
    }   
    }
